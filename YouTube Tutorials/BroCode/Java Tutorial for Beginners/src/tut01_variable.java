public class tut01_variable {
    public static void main(String[] args) {

        int x = 123; // declaring & initializing
        long y = 89437589342L;
        byte z = 100;
        float p = (float) 3.14;
        boolean q = true;
        char symbol = 'R';
        String name = "Raven";

        System.out.println("My number is: " + x);
        System.out.println("My large number is: " + y);
        System.out.println("My smaller number is: " + z);
        System.out.println("Pi is: " + p);
        System.out.println(q);
        System.out.println(symbol);
        System.out.println("Hello, my name is " + name);
    }

}
