public class tut04_expressions {
    public static void main(String[] args) {

        // expressions = operands & operators
        // operand = values, variables, numbers, quantity
        // operators = + - * %

        double friends = 10;

        // friends = (friends / 2);
        friends++; // shorthand increment
        friends--; // shorthand decrement

        // Cast value as different data type

        friends = (double)friends / 3;


        System.out.println(friends);
    }
}
