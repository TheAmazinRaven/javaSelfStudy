public class exercise01yUsingArrays {
    public static void main(String[] args) {

        double dailyTemp[] = {87.5, 90.0, 91.2, 88.8, 87.6, 87.5, 89.2};

        System.out.println("The first day, my temperature was " + dailyTemp[0] + " degrees F.");
        System.out.println("The second day, my temperature was " + dailyTemp[1] + " degrees F.");
        System.out.println("The third day, my temperature was " + dailyTemp[2] + " degrees F.");
        System.out.println("The fourth day, my temperature was " + dailyTemp[3] + " degrees F.");
        System.out.println("The fifth day, my temperature was " + dailyTemp[4] + " degrees F.");
        System.out.println("The sixth day, my temperature was " + dailyTemp[5] + " degrees F.");
        System.out.println("The seventh day, my temperature was " + dailyTemp[6] + " degrees F.");
    }
}

